class CarDetails {
  final String brand;
  final String model;
  final String variant;
  final String type;
  final String number;
  final String color;
  final String id;

  CarDetails({
    required this.id,
    required this.brand,
    required this.model,
    required this.variant,
    required this.type,
    required this.number,
    required this.color,
  });
}
